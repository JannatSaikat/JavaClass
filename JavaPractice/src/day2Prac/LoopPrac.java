package day2Prac;

public class LoopPrac {
	
	public static void main(String[] args) {
		
		
	
		
		int i = 21;
		
		while (i < 31) {
			
			System.out.println(i);
			
			i++;
		}
		
		System.out.println("==================");
		
		for (int n = 21; n < 31; n++ ) {
			
			System.out.println(n);
		}
		
		//============
		
	
				
				
		
		
	}

}
